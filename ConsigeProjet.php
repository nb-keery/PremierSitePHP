Un client veut site de vente d'appartement comme "se loger", ce site doit être fait en HTML, CSS et PHP

Le menu du site se compose de trois éléments :
		Accueil
		Annonces
		Contact

Dans la page d'Accueil, le client souhaite avoir deux menu, un en début de page et le second en fin de page
sous le premien menu le client veut une image de BONNE QUALITE qui prendra toute la longeur de la page web.
Sous son image le client aimerai voir une presentation rapide de son activité (pour la presentation soit créative
mais l'utilisation de lorem ipsum est accepté)
Dans le footer le client voudrai voir apparaitre le date de jour sous le format suivant "vendredi 20 decembre 2014".


Dans la page d'annonces le client voudrais voir s'afficher la liste de ses biens ('pour la liste de ses biens utiliser
le fichier immo.php ci-joint à ce document') pour chacun de ses biens , il veut voir le type de bien le nombre de pièce
la taille, la surface, le prix. il devra etre précisé si le bien est à louer ou à vendre (si le prix du bien est inférieur 
a 1000€, c'est une location).


Dans la contact, le client demande à voir figurer son adresse, une map le situant en france et un formulaire :
				- nom
				- adresse mail
				- Mobile
				- message
Tout les champs de ce formulaire devront tous être remplie, à l'envoie du formulaire un message s'affichera pour nous indiquer que
ce dernier a bien été transmis.


*****Pour ce projet le client ne nous a pas transmis le design, mais celui-ci attend une presentation de site assez attractive de votre part.

*****Pour faciliter la compréhension de votre code celui-ci doit etre correctement indenter, et si bessoin comprendre des commentaires pour l'explication